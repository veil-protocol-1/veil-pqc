/**
 * Generates PQC keypairs for the 3 Vultr API nodes + coordinator.
 * Outputs ONLY public keys to stdout. Private keys are written to
 * secrets/node-keys.local.env (gitignored) and never printed to terminal.
 *
 * Usage: node scripts/generate-node-keys.mjs
 */

import { writeFileSync } from 'fs';
import { fileURLToPath, pathToFileURL } from 'url';
import { dirname, resolve } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const walletDistPath = resolve(__dirname, '../packages/pqc-wallet/dist/index.js');

// Dynamic import with file URL so noble deps resolve from pqc-wallet/node_modules
const { generatePQCKeypair } = await import(pathToFileURL(walletDistPath).href);

function toHex(bytes) {
  return Array.from(bytes, b => b.toString(16).padStart(2, '0')).join('');
}

// 1 coordinator (ML-DSA-65) + 3 node (ML-KEM-768) keypairs
const coordinator = generatePQCKeypair();
const nodes = [generatePQCKeypair(), generatePQCKeypair(), generatePQCKeypair()];

// Public keys (safe to display)
const coordinatorPub = toHex(coordinator.publicKey.dsa);
const nodePubs = nodes.map(n => toHex(n.publicKey.kem));

// Private keys (never printed to terminal)
const coordinatorPriv = toHex(coordinator.signingKey);
const nodePrivs = nodes.map(n => toHex(n.encapsulationKey));

// ---- stdout: public keys ONLY ----
console.log('=== PUBLIC KEYS (safe to share) ===\n');
console.log(`VEIL_COORDINATOR_PUBKEY=${coordinatorPub}\n`);
nodes.forEach((_, i) => {
  console.log(`OCTRA_NODE_${i + 1}_PUBKEY=${nodePubs[i]}`);
});

// ---- Write private keys to gitignored secrets file ----
const secretsPath = resolve(__dirname, '../secrets/node-keys.local.env');

const secretsContent = [
  '# Node keys generated by scripts/generate-node-keys.mjs',
  '# DO NOT commit this file. It is gitignored.',
  '',
  `VEIL_COORDINATOR_PUBKEY=${coordinatorPub}`,
  `VEIL_COORDINATOR_PRIVATE_KEY=${coordinatorPriv}`,
  '',
  `OCTRA_NODE_1_PUBKEY=${nodePubs[0]}`,
  `VEIL_NODE_1_PRIVATE_KEY=${nodePrivs[0]}`,
  '',
  `OCTRA_NODE_2_PUBKEY=${nodePubs[1]}`,
  `VEIL_NODE_2_PRIVATE_KEY=${nodePrivs[1]}`,
  '',
  `OCTRA_NODE_3_PUBKEY=${nodePubs[2]}`,
  `VEIL_NODE_3_PRIVATE_KEY=${nodePrivs[2]}`,
].join('\n') + '\n';

writeFileSync(secretsPath, secretsContent, { encoding: 'utf8', mode: 0o600 });

console.log(`\n=== PRIVATE KEYS saved to: secrets/node-keys.local.env (gitignored, mode 600) ===`);
console.log('Private keys were NOT printed to terminal.');
